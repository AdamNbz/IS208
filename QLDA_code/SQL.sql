    MAPHIEU VARCHAR2(10) PRIMARY KEY,
    LOAI    VARCHAR2(10) CHECK (LOAI IN ('NHAP', 'XUAT')), -- loại phiếu
    NGAYLAP DATE DEFAULT SYSDATE,
